#!/usr/bin/env tsx
/**
 * Updates src/data/mlbSnapshot.ts with live data from the MLB Stats API.
 * The MLB Stats API is publicly accessible and does not require an auth token.
 *
 * Usage: npx tsx scripts/updateMlbSnapshot.ts
 *        npx tsx scripts/updateMlbSnapshot.ts --league-only
 */

import { writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { buildMlbSnapshot } from "../src/lib/mlbData";

async function main() {
  const skipTeamSnapshots = process.argv.includes("--league-only");
  console.log(
    `Fetching MLB snapshot from MLB Stats API${skipTeamSnapshots ? " (league only)" : ""}…`
  );
  const snapshot = await buildMlbSnapshot({ skipTeamSnapshots });
  const output = `import type { MlbSnapshot } from "@/types/mlb";

// Auto-generated by scripts/updateMlbSnapshot.ts on ${snapshot.updatedAt}
export const mlbSnapshot: MlbSnapshot = ${JSON.stringify(snapshot, null, 2)};
`;

  const outPath = resolve(__dirname, "../src/data/mlbSnapshot.ts");
  writeFileSync(outPath, output, "utf8");
  console.log(
    `Done. Wrote ${snapshot.teams.length} teams, ${snapshot.standings.length} standings rows, ${snapshot.recentGames.length} recent games, ${snapshot.upcomingGames.length} upcoming games, ${Object.keys(snapshot.teamSnapshots).length} team snapshots.`
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
