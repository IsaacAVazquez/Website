#!/usr/bin/env tsx
/**
 * Updates src/data/nbaSnapshot.ts with live data from ESPN's public NBA API.
 * No API key is required.
 *
 * Usage:
 *   npm run update:nba                # full refresh (standings + leaders + per-team schedules, ~1 min)
 *   npm run update:nba -- --league-only   # standings, leaders, scoreboard only (no per-team snapshots)
 */

import { config } from "dotenv";
import { writeFileSync } from "node:fs";
import { resolve } from "node:path";
config({ path: resolve(__dirname, "../.env.local") });
import { buildNbaSnapshot } from "../src/lib/nbaData";

async function main() {
  const leagueOnly = process.argv.includes("--league-only");
  console.log(
    leagueOnly
      ? "🏀 Fetching NBA snapshot (league-only: standings + leaders + scoreboard)…"
      : "🏀 Fetching NBA snapshot from ESPN…"
  );
  const snapshot = await buildNbaSnapshot({ skipTeamSnapshots: leagueOnly });

  const output = `import type { NbaSnapshot } from "@/types/nba";

// Auto-generated by scripts/updateNbaSnapshot.ts on ${snapshot.updatedAt}
export const nbaSnapshot: NbaSnapshot = ${JSON.stringify(snapshot, null, 2)};
`;

  const outPath = resolve(__dirname, "../src/data/nbaSnapshot.ts");
  writeFileSync(outPath, output, "utf8");
  const east = snapshot.teamsByConference.east.length;
  const west = snapshot.teamsByConference.west.length;
  console.log(
    `   NBA: East ${east} · West ${west} · ${snapshot.scorers.length} scorers · ${snapshot.recentFixtures.length} recent · ${snapshot.upcomingFixtures.length} upcoming · ${Object.keys(snapshot.teamSnapshots).length} team snapshots`
  );
}

main().catch((err) => {
  console.error("NBA snapshot update failed:", err);
  process.exit(1);
});
